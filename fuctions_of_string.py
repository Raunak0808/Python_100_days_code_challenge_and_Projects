a = "Hello World!"
print(type(a))
print(a)

#length function
print(len(a))

#count function
print(a.count("o"))

#upper function
print(a.upper())

#lower function
print(a.lower())

#index function
print(a.index("l"))

#capitalize function
print(a.capitalize())

#casefold function
print(a.casefold())

#find function
print(a.find("r"))

#format function
name = "Raunak"
b = "My name is, {}"
print(b.format(name))

#center function
print(name.center(10))